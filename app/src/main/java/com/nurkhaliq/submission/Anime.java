package com.nurkhaliq.submission;

public class Anime {
    private String name, remarks, photo, Detail, Airing, Synopsis, Episodes, Genre;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getDetail() { return Detail; }

    public void setDetail(String detail) { this.Detail = detail; }

    public String getAiring() { return Airing; }

    public void setAiring(String airing) { Airing = airing; }

    public String getSynopsis() { return Synopsis; }

    public void setSynopsis(String synopsis) { Synopsis = synopsis; }

    public String getEpisodes() { return Episodes; }

    public void setEpisodes(String episodes) { Episodes = episodes; }

    public String getGenre() { return Genre; }

    public void setGenre(String genre) { Genre = genre; }
}
